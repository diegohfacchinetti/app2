    public void limpaTabela(){
        while(((DefaultTableModel)this.jTable1.getModel()).getRowCount()> 0){
            ((DefaultTableModel)this.jTable1.getModel()).removeRow(0);
        }
    }
    
    public void prencheTabela(){
            ((DefaultTableModel)this.jTable1.getModel()).addRow(
                 new Object[]{prod,preco,qtd,total}
                );
            ItensCompra item = new ItensCompra();
            idCompras = Integer.parseInt(jTextField1.getText());
            item.setIdCompra(idCompras);
            item.setQtd(Integer.parseInt(qtd));
            item.setValor(Double.parseDouble(preco));
            item.setIdProd(idProd);
            todosItem.add(item);
    }
    
    
    
    
