      public void limpaTabela(){
        while(((DefaultTableModel)this.jTable1.getModel()).getRowCount()> 0){
            ((DefaultTableModel)this.jTable1.getModel()).removeRow(0);
        }
    }
      public void prencheTabela(){
            this.limpaTabela();
            C_Compras cc = new C_Compras();
            
            List<Object[]>lista = cc.consulta();
            
            for(Object[] item : lista){
                ((DefaultTableModel)this.jTable1.getModel()).addRow(
                 new Object[]{
                     item[0],
                     item[1],
                     item[2],
                     item[3]    
                 }
                );
            }
    }
