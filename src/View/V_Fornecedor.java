    private void LimpaCampos(){
        jtxtCnpj.setText("");
        jtxtFantasia.setText("");
        jtxtId.setText("");
        jtxtInscr.setText("");
        jtxtRazao.setText("");
        jtxtTelefone.setText("");

        jbtnExcluir.setVisible(false);
        jlblId.setVisible(false);
        jtxtId.setVisible(false);
        jtxtId.setEnabled(false);
    }
    
    private void HabilitaCampos(Boolean liga){
        jtxtCnpj.setEnabled(liga);
        jtxtFantasia.setEnabled(liga);
        jtxtInscr.setEnabled(liga);
        jtxtRazao.setEnabled(liga);
        jtxtTelefone.setEnabled(liga);
        
        
        jbtnExcluir.setVisible(liga);
    }
    
    private void PreencheCampos(){
        jtxtId.setText(fornecedor.getIdForn().toString());
        jtxtFantasia.setText(fornecedor.getFantasiaForn());
        jtxtInscr.setText(fornecedor.getInscrForn());
        jtxtRazao.setText(fornecedor.getRazaoForn());
        jtxtCnpj.setText(fornecedor.getCnpjForn());
        
        jlblId.setVisible(true);
        jtxtId.setVisible(true);
        jtxtId.setEnabled(false);
        HabilitaCampos(true);
        
    }
    
    private void estadoInicial(){
        LimpaCampos();
        HabilitaCampos(false);
        
    }
