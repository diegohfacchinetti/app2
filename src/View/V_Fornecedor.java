    private void jbtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSalvarActionPerformed
        Fornecedor p = null;
        if (fornecedor == null){
            p = new Fornecedor();
        }else{
            p = fornecedor;
        }
        p.setRazaoForn(jtxtRazao.getText());
        p.setFantasiaForn(jtxtFantasia.getText());
        p.setInscrForn(jtxtInscr.getText());
        p.setTelForn(jtxtTelefone.getText());
        p.setCnpjForn(jtxtCnpj.getText());
        //pegando o id

        C_Fornecedor cp = new C_Fornecedor();
        cp.salvar(p);
        
        estadoInicial();
    }//GEN-LAST:event_jbtnSalvarActionPerformed

