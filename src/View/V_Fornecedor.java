    private void jbtnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPesquisaActionPerformed
        V_Fornecedor_Listar tela_lista = new V_Fornecedor_Listar(null,true);
        tela_lista.setAlwaysOnTop(true);
        tela_lista.setLocationRelativeTo(this);
        tela_lista.setVisible(true);
        Object [] retorno = (Object[]) tela_lista.retornaFornSelecionado();
        if (retorno != null){
            Fornecedor forn = new Fornecedor();
            forn.setIdForn((Integer) retorno[0]);
            forn.setRazaoForn((String) retorno[1]);
            forn.setFantasiaForn((String) retorno[2]);
            forn.setCnpjForn((String) retorno[3]);
            forn.setInscrForn((String) retorno[4]);
            forn.setTelForn((String) retorno[5]);
            LimpaCampos();
            fornecedor = forn;
            PreencheCampos();
        }
    }//GEN-LAST:event_jbtnPesquisaActionPerformed

