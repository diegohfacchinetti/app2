    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Produto p = new Produto();        
        p.setDesProd(jTextPane1.getText());
        p.setCustoProd(Double.parseDouble(jTextPane2.getText()));
        p.setIndiceProd(BigDecimal.valueOf(Double.parseDouble(jTextPane3.getText())));
        p.setVendaProd(Double.parseDouble(jTextPane4.getText()));
        //pegando o id
        String forn = (String)jComboBox1.getSelectedItem();
        String[] vetor = forn.split(" ");
        Integer id = Integer.parseInt(vetor[0]);
        p.setFornProd(id);
        
        p.setCodProd(Integer.parseInt(jTextPane6.getText()));
        
        p.setEstoqueProd(Integer.parseInt(jTextPane5.getText()));
        
        C_Produto cp = new C_Produto();
        cp.atualizar(p);
    }//GEN-LAST:event_jButton1ActionPerformed

